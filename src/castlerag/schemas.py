class EvalQuestion(BaseModel):
    """Official CASTLE question record."""

    question_id: str
    query: str
    answers: Dict[str, str] = Field(
        description="{'a': ..., 'b': ..., 'c': ..., 'd': ...}"
    )
    ground_truth: Optional[AnswerChoice] = None

    @field_validator("answers")
    @classmethod
    def _validate_answers(cls, v: Dict[str, str]) -> Dict[str, str]:
        for key in ("a", "b", "c", "d"):
            if key not in v:
                raise ValueError(f"answers must contain key '{key}'")
        return v


class Prediction(BaseModel):
    """Per-question prediction with optional evidence trace."""

    question_id: str
    predicted_answer: AnswerChoice
    route: Optional[QuestionRoute] = None
    support_priors: Optional[Dict[str, float]] = None
    top_evidence_ids: List[str] = Field(default_factory=list)
    raw_answer_text: Optional[str] = None
    confidence: Optional[float] = None
