    absolute_start: int  # ms UTC
    absolute_end: int  # ms UTC
    source_video_path: str
    retrieval_clip_path: Optional[str] = None
    sampled_frame_paths: List[str] = Field(default_factory=list)
    transcript_text: Optional[str] = None
