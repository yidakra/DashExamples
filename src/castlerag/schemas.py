"""Shared typed models for CastleRAG pipeline records and outputs."""

from __future__ import annotations

from typing import Any, Dict, List, Literal, Optional

from pydantic import BaseModel, Field, field_validator

# ---------------------------------------------------------------------------
# Vocabulary types
# ---------------------------------------------------------------------------

SourceType = Literal[
    "main_clip",
    "main_event_summary",
    "transcript_window",
    "aux_heartrate",
    "aux_gaze",
    "aux_photo",
    "aux_thermal",
    "aux_video",
]

AuxSourceType = Literal[
    "aux_heartrate",
    "aux_gaze",
    "aux_photo",
    "aux_thermal",
    "aux_video",
]

Modality = Literal["video", "image", "text", "audio"]
CameraType = Literal["ego", "fixed"]
QuestionRoute = Literal["static_visual", "speech_text", "temporal", "mixed"]
AnswerChoice = Literal["a", "b", "c", "d"]


# ---------------------------------------------------------------------------
# Dataset records
# ---------------------------------------------------------------------------


class TranscriptSegment(BaseModel):
    """Raw ASR segment inside a transcript window."""

    start: float  # seconds, relative to hour
    end: float
    text: str


class TranscriptWindow(BaseModel):
    """Normalized transcript utterance window (≤15 s, ≤96 tokens).

    Used as the input unit for both BM25 lexical retrieval and OmniEmbed
    dense retrieval.  absolute_start/absolute_end are UTC Unix milliseconds.
    """

    transcript_window_id: str
    day: str
    camera_id: str
    camera_type: CameraType
    participant_id: Optional[str]
    room: Optional[str]
    hour: int
    transcript_text: str
    transcript_segments: List[TranscriptSegment] = Field(default_factory=list)
    speaker_hint: Optional[str] = None
    has_speech: bool = True
    transcript_char_len: int = 0
    absolute_start: int  # ms UTC
    absolute_end: int  # ms UTC
    version: str = "0.1.0"

    @field_validator("absolute_end")
    @classmethod
    def _end_after_start(cls, v: int, info: Any) -> int:
        start = (info.data or {}).get("absolute_start")
        if start is not None and v <= start:
            raise ValueError("absolute_end must be > absolute_start")
        return v


